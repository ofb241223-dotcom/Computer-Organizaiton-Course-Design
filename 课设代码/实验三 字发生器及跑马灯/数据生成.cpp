#include <bits/stdc++.h>

using namespace std;

int main()
{
    // 灯泡号清单
    vector<int> digits[10] = {
        {2, 3, 4, 5, 8, 11, 14, 17, 20, 23, 26, 27, 28, 29},         // 0
        {4, 10, 16, 22, 28},                                         // 1
        {2, 3, 4, 5, 11, 14, 15, 16, 17, 20, 26, 27, 28, 29},        // 2
        {2, 3, 4, 5, 11, 14, 15, 16, 17, 23, 26, 27, 28, 29},        // 3
        {2, 5, 8, 11, 14, 15, 16, 17, 23, 29},                       // 4
        {2, 3, 4, 5, 8, 14, 15, 16, 17, 23, 26, 27, 28, 29},         // 5
        {2, 3, 4, 5, 8, 14, 15, 16, 17, 20, 23, 26, 27, 28, 29},     // 6
        {2, 3, 4, 5, 11, 17, 23, 29},                                // 7
        {2, 3, 4, 5, 8, 11, 14, 15, 16, 17, 20, 23, 26, 27, 28, 29}, // 8
        {2, 3, 4, 5, 8, 11, 14, 15, 16, 17, 23, 26, 27, 28, 29}      // 9
    };

    FILE *f = fopen("Number_Display.dp", "w");
    fprintf(f, "Data:\n");

    for (int i = 0; i < 10; i++)
    {
        unsigned int val = 0;
        for (int bulb : digits[i])
        {
            // 核心逻辑：Bit = 灯泡号 - 1
            int bit = bulb - 1;
            if (bit >= 0 && bit < 32)
            {
                val |= (1U << bit);
            }
        }
        // 输出 8 位十六进制
        fprintf(f, "%08X\n", val);
    }

    fprintf(f, "Initial:\n0000\nFinal:\n0009\n");
    fclose(f);

    printf("Success!\n");
    return 0;
}